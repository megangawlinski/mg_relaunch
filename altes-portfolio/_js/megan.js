function toggle_about() {
 if(document.getElementById('dasbinich').style.display=='none') {
  document.getElementById('dasbinich').style.display='block';
  document.getElementById('hobbies').style.display='none';
  document.getElementById('vita').style.display='none';
  document.getElementById('arbeiten').style.display='none';
  document.getElementById('kontakt').style.display='none';

}
}
